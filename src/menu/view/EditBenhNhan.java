    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    
    // 1. Kiểm tra xem có hàng nào được chọn không
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, 
                "Vui lòng chọn một hàng để xóa.", 
                "Lỗi", 
                javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // 2. Lấy Mã Bệnh Nhân từ cột đầu tiên của hàng được chọn
    // Giả sử cột đầu tiên (index 0) là "Mã BN"
    String maBNCanXoa = (String) jTable1.getValueAt(selectedRow, 0); 
    
    // 3. Hiển thị hộp thoại xác nhận
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Bạn có chắc chắn muốn xóa bệnh nhân có Mã: " + maBNCanXoa + "?", 
            "Xác nhận xóa", 
            javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        // 4. Gọi hàm xóa từ lớp quản lý
        boolean success = manager.xoaBN(maBNCanXoa);
        
        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Đã xóa thành công bệnh nhân có Mã: " + maBNCanXoa);
            // 5. Cập nhật lại bảng để hiển thị kết quả
            loadTableData(); 
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Không tìm thấy bệnh nhân để xóa.", "Lỗi", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

