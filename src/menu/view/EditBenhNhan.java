    public void loadTableData() {
    // Tên cột thực tế (Phải khớp với BenhNhan.toObjectArray())
    String[] columnNames = {
        "Mã BN", "Tên Bệnh Nhân", "Ngày Sinh", "Giới Tính", "SĐT", "Ngày Vào Viện", "Bệnh Lý"
    };
    
    // Tạo Model mới với cột thực tế
    DefaultTableModel model = new DefaultTableModel(columnNames, 0) {
        @Override
        public boolean isCellEditable(int row, int column) {
           return false;
        }
    };
    
    // Lấy dữ liệu từ Controller (manager)
    List<BenhNhan> benhNhanList = manager.getAllBenhNhan();
    for (BenhNhan bn : benhNhanList) {
        // Gọi hàm đã viết trong BenhNhan.java
        model.addRow(bn.toObjectArray()); 
    }
    
    // Gán Model mới cho JTable để hiển thị dữ liệu thực
    jTable1.setModel(model); 
    jTable1.setRowHeight(25);
}

